const plus = (num1, num2) => {
  return num1 + num2;
};

const minus = (num1, num2) => {
  return num1 - num2;
};

const multiple = (num1, num2) => {
  return num1 * num2;
};

const divide = (num1, num2) => {
  return num1 / num2;
};

const calculator = (num1, num2) => {
  plus(num1, num2);
  minus(num1, num2);
  multiple(num1, num2);
  divide(num1, num2);
};
